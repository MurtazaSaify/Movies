import Foundation
import UIKit

extension UIColor {

    static var darkGreen: UIColor {
        return UIColor(red: 0.0/255.0, green: 100.0/255.0, blue: 0.0/255.0, alpha: 1)
    }

    static var darkYellow: UIColor {
        return UIColor(red: 204.0/255.0, green: 204.0/255.0, blue: 0.0/255.0, alpha: 1)
    }
}
